//------------------for----------------------
//print -10 to 10 number 
void main(){
  int n=-10,n1=10;
 for(int i=n;i<=n1;i++){
   print("$i");
 }
}

//------------------do while----------------------
void main(){
  int n=-10;
 do{
   print("$n");
   n++;
 }while(n<=10);
}

//------------------while----------------------
void main(){
  int n=-10;
 while(n<=10){
   print("$n");
   n++;
 }
}